function gallonsToLiters(gallons) {
  return gallons * 3.785;
}

function celciusToFarenheight(celcius) {
  return (celcius * (9 / 5)) + 32;
}

function cupsToTablespoons(cups) {
  return cups * 16;
}

function cupsToTeaspoons (cups) {
  return cups * 48;
}

function tablespoonsToTeaspoons(tablespoons) {
  return tablespoons * 3;
}